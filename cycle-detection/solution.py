def has_cycle(head):
    HAS_CYCLE = 1
    NO_CYCLES = 0

    # Keep track of what nodes we have already visited.
    nodes_visited = []

    # Constraint: 0 <= list size <= 1000
    for i in range(0, 1000):
        # If we encounter the end of the list, then there are no cycles.
        if(head == None):
            return NO_CYCLES
        
        # If we encounter a node with data that we have seen before,
        # then there is a cycle.
        if head in nodes_visited:
            return HAS_CYCLE

        nodes_visited.append(head)
        head = head.next    

    return HAS_CYCLE

